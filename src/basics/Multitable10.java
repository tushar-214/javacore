package basics;

public class Multitable10
{
    public static void main(String[] args) {
        int num=10;
        System.out.println("print table of "+ num);
        System.out.println("------------");
        for (int i=0;i<=10;i++)
        {
            System.out.println(num+" * "+i+" = "+ num*i);
        }
    }
}
